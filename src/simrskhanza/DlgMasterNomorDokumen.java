    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        if (Tkd.getText().trim().equals("")) {
            Valid.textKosong(Tkd, "Kode Dokumen");
            Tkd.requestFocus();
        } else if (TjnsDokumen.getText().trim().equals("")) {
            Valid.textKosong(TjnsDokumen, "Jenis Dokumen");
            TjnsDokumen.requestFocus();
        } else if (Tnomor.getText().trim().equals("")) {
            Valid.textKosong(Tnomor, "Nomor Dokumen");
            Tnomor.requestFocus();
        } else {
            Sequel.menyimpan("nomor_dokumen", "'" + Tkd.getText() + "','" + TjnsDokumen.getText() + "',"
                    + "'" + Tnomor.getText() + "','" + Valid.SetTgl(TtglBerlaku.getSelectedItem() + "") + "',"
                    + "'" + cmbStatus.getSelectedItem().toString() + "'", "Dokumen");
            emptTeks();
            BtnCariActionPerformed(null);
        }
}//GEN-LAST:event_BtnSimpanActionPerformed

