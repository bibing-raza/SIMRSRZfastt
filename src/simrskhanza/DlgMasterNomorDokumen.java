    private void BtnGantiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGantiActionPerformed
        if (Tkd.getText().trim().equals("")) {
            Valid.textKosong(Tkd, "Kode Dokumen");
            Tkd.requestFocus();
        } else if (TjnsDokumen.getText().trim().equals("")) {
            Valid.textKosong(TjnsDokumen, "Jenis Dokumen");
            TjnsDokumen.requestFocus();
        } else if (Tnomor.getText().trim().equals("")) {
            Valid.textKosong(Tnomor, "Nomor Dokumen");
            Tnomor.requestFocus();
        } else {            
            Sequel.mengedit("nomor_dokumen", "kd_nomor='" + Tkd.getText() + "'",
                    "jns_dokumen='" + TjnsDokumen.getText() + "', nomor_dokumen='" + Tnomor.getText() + "', "
                    + "tgl_berlaku='" + Valid.SetTgl(TtglBerlaku.getSelectedItem() + "") + "', "
                    + "status_nomor='" + cmbStatus.getSelectedItem().toString() + "'");
            emptTeks();
            BtnCariActionPerformed(null);
        }
}//GEN-LAST:event_BtnGantiActionPerformed

