    public void tampil() {     
        Valid.tabelKosong(tabMode);
        try {
            ps = koneksi.prepareStatement("SELECT * FROM nomor_dokumen WHERE "
                    + "jns_dokumen LIKE ? or "
                    + "nomor_dokumen like ? or "
                    + "status_nomor like ? ORDER BY kd_nomor");
            try {
                ps.setString(1, "%" + TCari.getText().trim() + "%");
                ps.setString(2, "%" + TCari.getText().trim() + "%");
                ps.setString(3, "%" + TCari.getText().trim() + "%");
                rs = ps.executeQuery();                
                while (rs.next()) {
                    tabMode.addRow(new String[]{                        
                        rs.getString("kd_nomor"),
                        rs.getString("jns_dokumen"),
                        rs.getString("nomor_dokumen"),
                        rs.getString("tgl_berlaku"),
                        rs.getString("status_nomor")
                    });
                }
                this.setCursor(Cursor.getDefaultCursor());
            } catch (Exception e) {
                System.out.println(e);
            } finally {
                if (rs != null) {
                    rs.close();
                }
                if (ps != null) {
                    ps.close();
                }
            }
        } catch (SQLException e) {
            System.out.println("Notifikasi : " + e);
        }
        LCount.setText("" + tabMode.getRowCount());
    }
    
    public void emptTeks() {
        Valid.autoNomer("nomor_dokumen", "ND", 4, Tkd);
        TjnsDokumen.setText("");
        TjnsDokumen.requestFocus();
        Tnomor.setText("");
        TtglBerlaku.setDate(new Date());        
        cmbStatus.setSelectedIndex(0);        
    }

    private void getData() {
        if (tbDokumen.getSelectedRow() != -1) {
            Tkd.setText(tbDokumen.getValueAt(tbDokumen.getSelectedRow(), 0).toString());
            TjnsDokumen.setText(tbDokumen.getValueAt(tbDokumen.getSelectedRow(), 1).toString());
            Tnomor.setText(tbDokumen.getValueAt(tbDokumen.getSelectedRow(), 2).toString());
            Valid.SetTgl(TtglBerlaku, tbDokumen.getValueAt(tbDokumen.getSelectedRow(), 3).toString());
            cmbStatus.setSelectedItem(tbDokumen.getValueAt(tbDokumen.getSelectedRow(), 4).toString());          
        }
    }
    
    public void isForm(){
        if (ChkInput.isSelected() == true) {
            ChkInput.setVisible(false);
            PanelInput.setPreferredSize(new Dimension(WIDTH, 120));
            FormInput.setVisible(true);
            ChkInput.setVisible(true);
        } else if (ChkInput.isSelected() == false) {
            ChkInput.setVisible(false);
            PanelInput.setPreferredSize(new Dimension(WIDTH, 20));
            FormInput.setVisible(false);
            ChkInput.setVisible(true);
        }
    }
    
