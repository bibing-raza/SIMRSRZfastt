    private void getData() {
        if (tbKeluhan.getSelectedRow() != -1) {
            kdpermasalahan.setText(tbKeluhan.getValueAt(tbKeluhan.getSelectedRow(), 0).toString());
            deskipsiKeluhan.setText(tbKeluhan.getValueAt(tbKeluhan.getSelectedRow(), 1).toString());
            cmbKategori.setSelectedItem(tbKeluhan.getValueAt(tbKeluhan.getSelectedRow(), 2).toString());          
        }
    }
    
    public void isForm(){
        if (ChkInput.isSelected() == true) {
            ChkInput.setVisible(false);
            PanelInput.setPreferredSize(new Dimension(WIDTH, 130));
            FormInput.setVisible(true);
            ChkInput.setVisible(true);
        } else if (ChkInput.isSelected() == false) {
            ChkInput.setVisible(false);
            PanelInput.setPreferredSize(new Dimension(WIDTH, 20));
            FormInput.setVisible(false);
            ChkInput.setVisible(true);
        }
    }
    
