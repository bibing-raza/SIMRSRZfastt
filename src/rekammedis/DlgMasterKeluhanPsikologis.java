    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        if (kdpermasalahan.getText().trim().equals("")) {
            Valid.textKosong(kdpermasalahan, "kode keluhan permasalahan");
            kdpermasalahan.requestFocus();
        } else if (deskipsiKeluhan.getText().trim().equals("")) {
            Valid.textKosong(deskipsiKeluhan, "Deskipsi keluhan permasalahan");
            deskipsiKeluhan.requestFocus();
        } else if (cmbKategori.getSelectedIndex() == 0) {
            Valid.textKosong(cmbKategori, "Kategori keluhan");
            cmbKategori.requestFocus();
        } else {
            jnskategori = "";
            if (cmbKategori.getSelectedIndex() == 1) {
                jnskategori = "umum_dewasa";
            } else if (cmbKategori.getSelectedIndex() == 2) {
                jnskategori = "anak_remaja";
            }
            
            Sequel.menyimpan("psikolog_master_keluhan", "'" + kdpermasalahan.getText() + "','" + jnskategori + "',"
                    + "'" + deskipsiKeluhan.getText() + "'", "Keluhan Permasalahan Psikologis");
            emptTeks();
            BtnCariActionPerformed(null);
        }
}//GEN-LAST:event_BtnSimpanActionPerformed

