    public void tampil(String ktgr) {     
        Valid.tabelKosong(tabMode);
        try {
            ps = koneksi.prepareStatement("SELECT kd_permasalahan, deskripsi_keluhan, kategori FROM psikolog_master_keluhan WHERE "
                    + "kategori like '%" + ktgr + "%' and deskripsi_keluhan LIKE ? or "
                    + "kategori like '%" + ktgr + "%' and kd_permasalahan like ? or "
                    + "kategori like '%" + ktgr + "%' and kategori like ? ORDER BY kd_permasalahan");

            try {
                ps.setString(1, "%" + TCari.getText().trim() + "%");
                ps.setString(2, "%" + TCari.getText().trim() + "%");
                ps.setString(3, "%" + TCari.getText().trim() + "%");
                rs = ps.executeQuery();                
                while (rs.next()) {
                    tabMode.addRow(new Object[]{                        
                        rs.getString(1),
                        rs.getString(2),
                        rs.getString(3).replaceAll("umum_dewasa", "Umum / Dewasa").replaceAll("anak_remaja", "Anak / Remaja")
                    });
                }
                this.setCursor(Cursor.getDefaultCursor());
            } catch (Exception e) {
                System.out.println("rekammedis.DlgMasterKeluhanPsikologis.tampil() : " + e);
            } finally {
                if (rs != null) {
                    rs.close();
                }
                if (ps != null) {
                    ps.close();
                }
            }
        } catch (SQLException e) {
            System.out.println("Notifikasi : " + e);
        }
        LCount.setText("" + tabMode.getRowCount());
    }
    
