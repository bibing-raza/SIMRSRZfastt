    private void tampil() {
        Valid.tabelKosong(tabMode);
        try {
            ps = koneksi.prepareStatement("select * from master_kasus_persalinan_dinkes where kode like ? or nama_kasus like ? order by kode");
            try {
                ps.setString(1, "%" + TCari.getText().trim() + "%");
                ps.setString(2, "%" + TCari.getText().trim() + "%");
                rs = ps.executeQuery();
                while (rs.next()) {
                    tabMode.addRow(new String[]{
                        rs.getString(1), 
                        rs.getString(2)
                    });
                }
            } catch (Exception e) {
                System.out.println("Notif : " + e);
            } finally {
                if (rs != null) {
                    rs.close();
                }
                if (ps != null) {
                    ps.close();
                }
            }
        } catch (Exception e) {
            System.out.println("Notifikasi : " + e);
        }
        LCount.setText("" + tabMode.getRowCount());
    }

    private void emptTeks() {
        kode.setText("");
        kode.requestFocus();
        nmkasus.setText("");
        TCari.setText("");        
        Valid.autoNomer(tabMode, "", 3, kode);
    }

    private void getData() {
        if(tbKasus.getSelectedRow()!= -1){
            kode.setText(tbKasus.getValueAt(tbKasus.getSelectedRow(),0).toString());
            nmkasus.setText(tbKasus.getValueAt(tbKasus.getSelectedRow(),1).toString());
        }
    }
    
