    private void getData1() {
        if(tbAktif.getSelectedRow()!= -1){  
            kode.setText(tbAktif.getValueAt(tbAktif.getSelectedRow(),0).toString()); 
            nmRejimen.setText(tbAktif.getValueAt(tbAktif.getSelectedRow(),1).toString()); 
            cmbStatus.setSelectedItem(tbAktif.getValueAt(tbAktif.getSelectedRow(),2).toString());            
        }
    }
    
