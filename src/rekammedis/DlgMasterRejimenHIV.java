    public void tampilAktif() {     
        Valid.tabelKosong(tabMode);
        try {
            ps = koneksi.prepareStatement("SELECT kd_rejimen, nm_rejimen, IF(flag='1','Aktif','Non Aktif') status FROM hiv_master_rejimen "
                    + "where flag='1' and nm_rejimen like '%" + TCari.getText().trim() + "%' order by kd_rejimen");
            try {
                rs = ps.executeQuery();
                while (rs.next()) {
                    tabMode.addRow(new Object[]{
                        rs.getString("kd_rejimen"),
                        rs.getString("nm_rejimen"),
                        rs.getString("status")
                    });
                }
                this.setCursor(Cursor.getDefaultCursor());
            } catch (Exception e) {
                System.out.println("rekammedis.DlgMasterRejimenHIV.tampilAktif() : " + e);
            } finally {
                if (rs != null) {
                    rs.close();
                }
                if (ps != null) {
                    ps.close();
                }
            }
        } catch (SQLException e) {
            System.out.println("Notifikasi : " + e);
        }
        LCount.setText("" + tabMode.getRowCount());
    }
    
