    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        if (kode.getText().trim().equals("")) {
            Valid.textKosong(kode, "kode rejimen");
        } else if (nmRejimen.getText().trim().equals("")) {
            Valid.textKosong(nmRejimen, "Nama Rejimen ART");
        } else {
            Valid.autoNomer("hiv_master_rejimen", "R", 2, kode);
            status = "";
            if (cmbStatus.getSelectedIndex() == 0) {
                status = "1";
            } else if (cmbStatus.getSelectedIndex() == 1) {
                status = "0";
            }
            
            Sequel.menyimpan("hiv_master_rejimen", "'" + kode.getText() + "','" + nmRejimen.getText() + "','" + status + "'", "Master Nama Rejimen ART");
            emptTeks();
            BtnCariActionPerformed(null);
        }
}//GEN-LAST:event_BtnSimpanActionPerformed

